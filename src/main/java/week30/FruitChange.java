package week30;

public class FruitChange {
    public int getMinFruit(int[] fruits){
        int min = 100;
        for (int fruit : fruits) {
            min = Math.min(fruit, min);
        }

        return min;
    }

    public boolean isMinUnique(int[] fruits){
        int min = getMinFruit(fruits);
        int cnt = 0;
        for (int fruit : fruits) {
            if(fruit == min) cnt++;
        }

        return (cnt == 1);
    }

    public int getMinIdx(int[] fruits){
        int min = getMinFruit(fruits);
        for (int i = 0; i < 3; i++) {
            if(min == fruits[i]){
                return i;
            }
        }

        return 0;
    }

    public int solution(int[][] fruit){
        int answer = 0;
        int[] ch = new int[fruit.length];
        for(int i = 0; i < fruit.length; i++){
            if(ch[i] == 1) continue; //이미 교환한 사람
            if(!isMinUnique(fruit[i])) continue;
            for(int j = i + 1; j < fruit.length; j++){
                if(ch[j] == 1) continue;
                if(!isMinUnique(fruit[j])) continue;
                int a = getMinIdx(fruit[i]);
                int b = getMinIdx(fruit[j]);
                if(a != b && fruit[i][b] > 0 && fruit[j][a] > 0){
                    if(fruit[i][a] + 1 <= fruit[i][b] - 1 && fruit[j][b] + 1 <= fruit[j][a] - 1){
                        fruit[i][a]++;
                        fruit[i][b]--;
                        fruit[j][b]++;
                        fruit[j][a]--;
                        ch[i] = 1;
                        ch[j] = 1;
                        break;
                    }
                }

            }
        }

        for (int[] x : fruit) {
            int min = getMinFruit(x);
            answer += min;
        }

        return answer;
    }

    public static void main(String[] args){
        FruitChange T = new FruitChange();
        System.out.println(T.solution(new int[][]{{10, 20, 30}, {12, 15, 20}, {20, 12, 15}, {15, 20, 10}, {10, 15, 10}}));
        System.out.println(T.solution(new int[][]{{10, 9, 11}, {15, 20, 25}}));
        System.out.println(T.solution(new int[][]{{0, 3, 27}, {20, 5, 5}, {19, 5, 6}, {10, 10, 10}, {15, 10, 5}, {3, 7, 20}}));
        System.out.println(T.solution(new int[][]{{3, 7, 20}, {10, 15, 5}, {19, 5, 6}, {10, 10, 10}, {15, 10, 5}, {3, 7, 20}, {12, 12, 6}, {10, 20, 0}, {5, 10, 15}}));
    }
}
